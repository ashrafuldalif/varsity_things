#include<iostream>
using namespace std;
int main(){

// binary and
    cout<<"binary and\t"<<(9&&8)<<endl;

// binary or
cout<<" binary or\t"<<(9|8)<<endl;

// binary xor
cout<<" binary xor\t"<<(9^8)<<endl;

// binary ones complement
cout <<"binary ones complement and\t"<<~9<<endl;

// binary shift left
cout<<" binary shift left\t"<<(9<<1)<<endl;

// binary shift right
cout<<" binary shift right\t"<<(9>>1)<<endl;

    

}